#class: Activity

class Activity:
    def __init__(self):
        pass

    def setUserId(self, userId):
        self.userId = userId

    def setActivityType(self, activityType):
        self.activityType = activityType

    def setStartDateTime(self, startDateTime):
        self.startDateTime = startDateTime

    def setEndDateTime(self, endDateTime):
        self.endDateTime = endDateTime

    def setDistance(self, distance):
        self.distance = distance

    def setPace(self, pace):
        self.pace = pace

    def setElevationGain(self, elevationGain):
        self.elevationGain = elevationGain

    def setEvidence(self, evidence):
        self.evidence = evidence

    def setPhoto(self, photo):
        self.photo = photo

    def setActivityId(self, activityId):
        self.activityId = activityId